#include "utils.h"

int		is_numeric_str(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]) && str[i] != '-')
			return (0);
		++i;
	}
	return (1);
}
